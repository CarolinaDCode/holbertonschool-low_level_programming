#include <stdio.h>
#include <stdlib.h>
/**
 *op_add - function that sum two  numbers.
 *@a: a
 *@b: b
 *Return: operation sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - function that add two  numbers.
 *@a: a
 *@b: b
 *Return: operation substrac
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - function that add two  numbers.
 *@a: a
 *@b: b
 *Return: operation multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - function that add two  numbers.
 *@a: a
 *@b: b
 *Return: operation divide
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - function that add two  numbers.
 *@a: a
 *@b: b
 *Return: operation modul
 */
int op_mod(int a, int b)
{
	return (a % b);
}
